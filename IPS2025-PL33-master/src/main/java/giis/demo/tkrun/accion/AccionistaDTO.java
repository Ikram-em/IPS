package giis.demo.tkrun.accion;

public class AccionistaDTO {

	private int id_accionista;
	private int id_user;
	private int n_acciones;
	

	public int getId_accionista() {
		return id_accionista;
	}

	public void setId_accionista(int id_accionista) {
		this.id_accionista = id_accionista;
	}

	public int getId_user() {
		return id_user;
	}

	public void setId_user(int id_user) {
		this.id_user = id_user;
	}

	public int getN_acciones() {
		return n_acciones;
	}

	public void setN_acciones(int n_acciones) {
		this.n_acciones = n_acciones;
	}

}
